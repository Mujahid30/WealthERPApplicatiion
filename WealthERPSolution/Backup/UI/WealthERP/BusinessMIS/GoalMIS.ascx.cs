        private void BindGoalListMIS()
        {
            DataSet dsGoalMis;
            DataTable dtGetGoalMIS;
            SetParameters();
            dsGoalMis = customerGoalSetupBo.GetGoalListMIS(userType, int.Parse(hdnadviserId.Value), int.Parse(hdnrmId.Value), customerId, int.Parse(hdnbranchheadId.Value), int.Parse(hdnbranchId.Value), int.Parse(hdnAll.Value), isGroup, hdnGoalCode.Value);
            dtGetGoalMIS = dsGoalMis.Tables[0];
            if (dtGetGoalMIS.Rows.Count > 0)
            {
                gvGoalMIS.DataSource = dtGetGoalMIS;
                gvGoalMIS.CurrentPageIndex = 0;
                gvGoalMIS.DataBind();
                gvGoalMIS.Visible = true;
                if (Cache["GoalMIS" + advisorVo.advisorId] == null)
                {
                    Cache.Insert("GoalMIS" + userVo.UserId, dtGetGoalMIS);
                }
                else
                {
                    Cache.Remove("GoalMIS" + userVo.UserId);
                    Cache.Insert("GoalMIS" + userVo.UserId, dtGetGoalMIS);
                }
                tdGoalExport.Visible = true;
                ErrorMessage.Visible = false;
                tdGoalExport.Visible = true;
                tbl.Visible = true;
                btnGoalMIS.Visible = true;
            }
            else
            {
                gvGoalMIS.Visible = false;
                ErrorMessage.Visible = true;
                tdGoalExport.Visible = false;
                tdGoalExport.Visible = false;
                tbl.Visible = false;
                btnGoalMIS.Visible = false;
            }
        }

