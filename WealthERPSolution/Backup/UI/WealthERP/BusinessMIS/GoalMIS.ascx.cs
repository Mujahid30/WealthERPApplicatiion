        private void BindGoal()
        {
            DataSet dsGoals = new DataSet();
            DataTable dtGoals;
            dsGoals = customerGoalSetupBo.GetGoals();
            dtGoals = dsGoals.Tables[0];
            if (dtGoals.Rows.Count > 0)
            {
                ddlGoal.DataSource = dtGoals;
                ddlGoal.DataValueField = dtGoals.Columns["XG_GoalCode"].ToString();
